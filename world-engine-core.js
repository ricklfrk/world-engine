  function getStoryPromptBlock(state) {
    if (!state || !state.storyType || !state.storyType.template) return '';
    var tmpl = null;
    for (var i = 0; i < STORY_TEMPLATES.length; i++) {
      if (STORY_TEMPLATES[i].id === state.storyType.template) { tmpl = STORY_TEMPLATES[i]; break; }
    }
    if (!tmpl) return '';
    var phaseIdx = state.storyType.currentPhase || 0;
    if (phaseIdx >= tmpl.phases.length) phaseIdx = tmpl.phases.length - 1;
    var phaseName = tmpl.phases[phaseIdx] || '';
    var toneName = 'natural';
    if (state.storyType.tone === 'custom' && state.storyType.customToneText) {
      toneName = state.storyType.customToneText;
    } else {
      for (var j = 0; j < EMOTIONAL_TONES.length; j++) {
        if (EMOTIONAL_TONES[j].id === state.storyType.tone) { toneName = EMOTIONAL_TONES[j].name; break; }
      }
    }
    var worldDesc = state.worldDescription || '';
    if (worldDesc.length > 300) {
      worldDesc = Array.from(worldDesc).slice(0, 300).join('');
    }
    var block = '【故事方向】\n';
    block += '📖 故事脉络：' + tmpl.name + ' → ' + phaseName + '\n';
    block += '💫 情感基调：' + toneName + '\n';
    if (worldDesc) block += '🌍 世界设定：' + worldDesc + '\n';
    return block;
  }

