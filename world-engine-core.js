// ★ v3.0.0: 统计生命周期数据
function getLifecycleStats(worldState) {
  if (!worldState || !worldState.characterLifecycles) {
    return { total: 0, alive: 0, dead: 0, reborn: 0, dormant: 0 };
  }
  var stats = { total: 0, alive: 0, dead: 0, reborn: 0, dormant: 0 };
  for (var name in worldState.characterLifecycles) {
    stats.total++;
    var state = worldState.characterLifecycles[name].state;
    if (state === 'DEAD') stats.dead++;
    else if (state === 'REBORN') stats.reborn++;
    else if (state === 'DORMANT') stats.dormant++;
    else stats.alive++;
  }
  return stats;
}

