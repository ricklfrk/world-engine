function setCharacterLifecycle(state, charName, newState) {
  if (!state.characterLifecycles) state.characterLifecycles = {};
  if (!LIFECYCLE_STATES[newState]) return false;
  if (!state.characterLifecycles[charName]) {
    state.characterLifecycles[charName] = { state: 'ALIVE', lastChangedRound: 0, history: [] };
  }
  var prev = state.characterLifecycles[charName].state;
  state.characterLifecycles[charName].state = newState;
  state.characterLifecycles[charName].lastChangedRound = state.round;
  if (!state.characterLifecycles[charName].history) state.characterLifecycles[charName].history = [];
  state.characterLifecycles[charName].history.push({ from: prev, to: newState, round: state.round });
  if (state.characterLifecycles[charName].history.length > 20) {
    state.characterLifecycles[charName].history = state.characterLifecycles[charName].history.slice(-20);
  }
  saveState(state);
  return true;
}

// ★ v3.0.1: 成就分级稀有度
function getAchievementRarity(def) {
  if (!def) return 0;
  if (def.type === 'survival') {
    if ((def.threshold||0) >= 1000) return 4;
    if ((def.threshold||0) >= 500) return 3;
    if ((def.threshold||0) >= 100) return 2;
    if ((def.threshold||0) >= 50) return 1;
    return 0;
  }
  if (def.nsfw) return 0;
  if (def.type === 'meta') return 3;
  if (/100$|boss|end$|climax/.test(def.id||'')) return 3;
  if (def.type === 'story' || def.type === 'growth') return 2;
  if (/50$/.test(def.id||'')) return 2;
  return 1;
}

// ★ v3.0.0: 记忆统计
function getMemoryStats(worldState) {
  if (!worldState || !worldState.memories) return { total: 0, hot: 0, cold: 0 };
  var config = worldState.settings || worldState.config || {};
  var hot = 0, cold = 0;
  for (var mi = 0; mi < worldState.memories.length; mi++) {
    worldState.memories[mi].lastAccessRound = worldState.memories[mi].lastAccessRound || 0;
    if (isHotMemory(worldState.memories[mi], worldState.round, config)) hot++;
    else cold++;
  }
  return { total: worldState.memories.length, hot: hot, cold: cold };
}

