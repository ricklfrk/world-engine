  function getSavepointKey() {
    return 'world_enginesavepoint_' + getChatId();
  }
  const MAX_SP = 30;

  function saveSavepoint(state) {
    if (typeof SillyTavern === 'undefined') return;
    try {
      var ctx = SillyTavern.getContext();
      if (!ctx || !ctx.chat) return;
      var chatLen = ctx.chat.length;
      var sps = getSavepoints();
      // 相同 chatLen 不重复保存（避免同一位置多次推演覆盖）
      if (sps.length > 0 && sps[sps.length - 1].chatLen === chatLen) return;
      sps.push({
        chatLen: chatLen,
        state: JSON.parse(JSON.stringify(state)),
        round: state.round,
        time: Date.now()
      });
      // 超过上限裁剪旧记录
      if (sps.length > MAX_SP) sps.splice(0, sps.length - MAX_SP);
      window.WORLD_ENGINE_STORAGE.setItem(getSavepointKey(), JSON.stringify(sps));
    } catch(e) { console.warn('[World Engine] saveSavepoint 失败', e); }
  }

  function getSavepoints() {
    try { return JSON.parse(window.WORLD_ENGINE_STORAGE.getItem(getSavepointKey()) || '[]'); } catch(e) { return []; }
  }

  function rollbackToChatLength(targetLen) {
    try {
      var sps = getSavepoints();
      if (sps.length === 0) return null;
      // 找到最新且 chatLen <= targetLen 的快照
      var idx = -1;
      for (var i = sps.length - 1; i >= 0; i--) {
        if (sps[i].chatLen <= targetLen) { idx = i; break; }
      }
      if (idx === -1) return null;
      // 还原状态
      var restoredChatLen = sps[idx].chatLen;
      saveState(sps[idx].state);
      // B3 fix: 移除被恢复的 savepoint，使下次 evolve() 时 dedup 不拦截新快照写入
      var newSps = sps.slice(0, idx + 1).filter(function(sp) { return sp.chatLen < restoredChatLen; });
      window.WORLD_ENGINE_STORAGE.setItem(getSavepointKey(), JSON.stringify(newSps));
      return sps[idx].state;
    } catch(e) { console.warn('[World Engine] rollbackToChatLength 失败', e); return null; }
  }

  function clearSavepoints() {
    window.WORLD_ENGINE_STORAGE.removeItem(getSavepointKey());
  }

