function checkCombatAchievements(state, combatEntry) {
  if (!state.achievements || !combatEntry) return [];
  var newly = [];
  var ct = state.combat;
  if (!ct) return newly;
  var totalKills = ct.totalKills || 0;
  var wins = ct.wins || 0;
  var bosses = ct.bossesDefeated ? ct.bossesDefeated.length : 0;
  if (totalKills >= 5 && !state.achievements.unlocked['kill_5']) { if (unlockAchievement(state, 'kill_5', '击杀5个敌人')) newly.push('kill_5'); }
  if (totalKills >= 200 && !state.achievements.unlocked['kill_200']) { if (unlockAchievement(state, 'kill_200', '击杀200人')) newly.push('kill_200'); }
  if (totalKills >= 500 && !state.achievements.unlocked['kill_500']) { if (unlockAchievement(state, 'kill_500', '击杀500人')) newly.push('kill_500'); }
  if ((ct.bestStreak || 0) >= 5 && !state.achievements.unlocked['win_5']) { if (unlockAchievement(state, 'win_5', '5连胜')) newly.push('win_5'); }
  if ((ct.bestStreak || 0) >= 10 && !state.achievements.unlocked['win_10']) { if (unlockAchievement(state, 'win_10', '10连胜')) newly.push('win_10'); }
  if ((ct.bestStreak || 0) >= 20 && !state.achievements.unlocked['win_20']) { if (unlockAchievement(state, 'win_20', '20连胜')) newly.push('win_20'); }
  if (bosses >= 5 && !state.achievements.unlocked['boss_5']) { if (unlockAchievement(state, 'boss_5', '击败5个BOSS')) newly.push('boss_5'); }
  if ((ct.totalDamageTaken || 0) >= 500 && !state.achievements.unlocked['defense_master']) { if (unlockAchievement(state, 'defense_master', '承受500伤害')) newly.push('defense_master'); }
  if ((ct.noHitCount || 0) >= 5 && !state.achievements.unlocked['no_hit_5']) { if (unlockAchievement(state, 'no_hit_5', '5次无伤')) newly.push('no_hit_5'); }
  return newly;
}

function checkHiddenAchievements(state) {
  if (!state.achievements) return [];
  var newly = [];
  var ach = state.achievements;
  var totalAch = ach.totalUnlocked || 0;
  var round = state.round;
  // 万人嫌：所有 emotionMap 的 attitude 都是敌意
  if (!ach.unlocked['hated_all'] && state.emotionMap) {
    var allHostile = true;
    var hasEntries = false;
    for (var ha in state.emotionMap) {
      hasEntries = true;
      if (state.emotionMap[ha].attitude !== '敌意' && state.emotionMap[ha].attitude !== '不共戴天') { allHostile = false; break; }
    }
    if (hasEntries && allHostile) { if (unlockAchievement(state, 'hated_all', '所有人都讨厌你')) newly.push('hated_all'); }
  }
  // 自杀王：死亡5次以上
  if (!ach.unlocked['suicide_king'] && (state.totalDeaths || 0) >= 5) {
    if (unlockAchievement(state, 'suicide_king', '死了5次')) newly.push('suicide_king');
  }
  // 反成就猎手：50轮内无解锁
  if (!ach.unlocked['no_achieve'] && round >= 50 && totalAch === 0) {
    if (unlockAchievement(state, 'no_achieve', '50轮零成就')) newly.push('no_achieve');
  }
  // 速度之星：10轮内3个成就
  if (!ach.unlocked['speed_star'] && round <= 10 && totalAch >= 3) {
    if (unlockAchievement(state, 'speed_star', '10轮3成就')) newly.push('speed_star');
  }
  // 全分类成就
  if (!ach.unlocked['all_category'] && totalAch >= 10) {
    var allCat = { survival:0, combat:0, intimate:0, quirky:0, exploration:0, social:0, story:0, growth:0, world:0, meta:0 };
    for (var ak in ach.unlocked) {
      if (ACHIEVEMENT_DEFS[ak]) {
        var cat = ACHIEVEMENT_DEFS[ak].type || 'meta';
        allCat[cat] = (allCat[cat] || 0) + 1;
      }
    }
    var catAll = true;
    for (var ca in allCat) { if (allCat[ca] === 0) { catAll = false; break; } }
    if (catAll) { if (unlockAchievement(state, 'all_category', '全分类成就')) newly.push('all_category'); }
  }
  return newly;
}

// v3.0.0: 导出全状态快照
function exportSnapshot() {
  const state = loadState();
  if (!state) return null;
  const snapshot = {
    version: '3.0.0',
    exportedAt: new Date().toISOString(),
    state: state,
    memory: (typeof window.WORLD_ENGINE_MEMORY !== 'undefined') ? window.WORLD_ENGINE_MEMORY.exportMemory ? window.WORLD_ENGINE_MEMORY.exportMemory() : null : null,
    settings: JSON.parse(window.WORLD_ENGINE_STORAGE.getItem('world_engine_settings') || '{}'),
    config: JSON.parse(window.WORLD_ENGINE_STORAGE.getItem('world_engine_config') || '{}'),
  };
  const jsonStr = JSON.stringify(snapshot, null, 2);
  const blob = new Blob([jsonStr], { type: 'application/json' });
  const url = URL.createObjectURL(blob);
  const a = document.createElement('a');
  a.href = url;
  a.download = `world-engine-backup-${state.round || 0}-${Date.now()}.json`;
  a.click();
  URL.revokeObjectURL(url);
  return jsonStr;
}

// v3.0.0: 从文件导入快照恢复
function importSnapshot(file) {
  return new Promise((resolve, reject) => {
    const reader = new FileReader();
    reader.onload = function(e) {
      try {
        const data = JSON.parse(e.target.result);
        if (!data.state) { reject(new Error('无效的快照文件')); return; }
        // 恢复 state
        // 使用与 saveState 一致的存储键（按聊天ID隔离）
        saveState(data.state);
        // 恢复设置
        if (data.settings) window.WORLD_ENGINE_STORAGE.setItem('world_engine_settings', JSON.stringify(data.settings));
        if (data.config) window.WORLD_ENGINE_STORAGE.setItem('world_engine_config', JSON.stringify(data.config));
        // 触发 UI 刷新
        if (window.WORLD_ENGINE_UI && window.WORLD_ENGINE_UI.refresh) window.WORLD_ENGINE_UI.refresh();
        resolve({ round: data.state.round || 0 });
      } catch(err) { reject(new Error('快照解析失败: ' + err.message)); }
    };
    reader.onerror = function() { reject(new Error('文件读取失败')); };
    reader.readAsText(file);
  });
}

function logEvent(state, type, sourceId, data) {
  if (!state.eventLog) state.eventLog = [];
  const entry = {
    type: type,
    sourceId: sourceId,
    round: state.round || 0,
    timestamp: Date.now(),
    data: data || {}
  };
  state.eventLog.push(entry);
  // 最多保留 200 条，防膨胀
  if (state.eventLog.length > 200) state.eventLog = state.eventLog.slice(-200);
}

