// ★ v3.0.0: 情感状态机 — 应用状态转移
function applyEmotionState(emotionMap, entity, eventType) {
  if (!emotionMap) return;
  if (!emotionMap[entity]) {
    emotionMap[entity] = { attitude: 0, emotion: 'NEUTRAL', lastInteraction: Date.now(), stateDuration: 0 };
  }
  var em = emotionMap[entity];
  var transition = EMOTION_TRANSITIONS[eventType] || EMOTION_TRANSITIONS.default;
  var rand = Math.random();
  var cumulative = 0;
  em.prevEmotion = em.emotion;
  for (var stateName in transition) {
    cumulative += transition[stateName];
    if (rand <= cumulative) {
      em.emotion = stateName;
      em.stateDuration = 0;
      break;
    }
  }
  em.lastInteraction = Date.now();
  // 按状态自动调整 attitude
  var stateInfo = EMOTION_STATES[em.emotion] || EMOTION_STATES.NEUTRAL;
  if (em.emotion === 'FRIENDLY' || em.emotion === 'JOYFUL') {
    em.attitude = Math.min(10, (em.attitude || 0) + 0.5);
  } else if (em.emotion === 'ANGRY' || em.emotion === 'ANNOYED' || em.emotion === 'FEARFUL') {
    em.attitude = Math.max(-10, (em.attitude || 0) - 0.5);
  }
  return em;
}

// ★ v3.0.0: 情感状态机 — 衰减（每轮调用）
function decayEmotionStates(emotionMap, currentRound) {
  if (!emotionMap) return;
  for (var name in emotionMap) {
    var em = emotionMap[name];
    em.stateDuration = (em.stateDuration || 0) + 1;
    // 超过 5 轮无交互则衰减到 NEUTRAL
    if (em.stateDuration > 5 && em.emotion !== 'NEUTRAL') {
      em.emotion = 'NEUTRAL';
      em.stateDuration = 0;
    }
    // attitude 缓慢趋近 0
    if (em.attitude > 0) em.attitude = Math.max(0, em.attitude - 0.2);
    else if (em.attitude < 0) em.attitude = Math.min(0, em.attitude + 0.2);
  }
}

// ★ v3.0.0: 获取情感状态描述
function getEmotionStateSummary(emotionMap) {
  if (!emotionMap) return '';
  var parts = [];
  for (var name in emotionMap) {
    var em = emotionMap[name];
    var stateInfo = EMOTION_STATES[em.emotion] || EMOTION_STATES.NEUTRAL;
    parts.push(name + ':' + stateInfo.name + '(' + em.attitude + ')');
  }
  return parts.join(' | ');
}

// ★ v3.0.0: 初始化角色生命周期
function initCharacterLifecycle(worldState, charName) {
  if (!worldState) return;
  if (!worldState.characterLifecycles) worldState.characterLifecycles = {};
  if (!worldState.characterLifecycles[charName]) {
    worldState.characterLifecycles[charName] = {
      state: 'ALIVE',
      birthRound: worldState.round || 0,
      deathRound: null,
      rebirthCount: 0,
      lastStateChange: worldState.round || 0,
      totalLifecycles: 0,
    };
  }
  return worldState.characterLifecycles[charName];
}

// ★ v3.0.0: 应用生命周期转换
function applyLifecycleTransition(worldState, charName, targetState) {
  if (!worldState || !charName || !targetState) return false;
  if (!worldState.characterLifecycles) worldState.characterLifecycles = {};
  var lc = worldState.characterLifecycles[charName];
  if (!lc) lc = initCharacterLifecycle(worldState, charName);

  var prevState = lc.state;
  lc.prevState = prevState;
  lc.state = targetState;
  lc.lastStateChange = worldState.round || 0;

  if (targetState === 'DEAD') {
    lc.deathRound = worldState.round || 0;
    lc.totalLifecycles++;
  }
  if (targetState === 'REBORN') {
    lc.rebirthCount = (lc.rebirthCount || 0) + 1;
    lc.totalLifecycles = (lc.totalLifecycles || 0) + 1;
    lc.birthRound = worldState.round || 0;
  }

  // 日志
  if (!worldState.eventLog) worldState.eventLog = [];
  worldState.eventLog.push({
    type: 'lifecycle',
    timestamp: Date.now(),
    round: worldState.round || 0,
    event: '生命周期: ' + charName + ' ' + prevState + ' → ' + targetState,
    entities: [charName],
  });
  return true;
}

