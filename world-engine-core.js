  function extractTags(text) {
    if (!text || typeof text !== 'string') return [];
    var tags = [];
    // 提取实体标记 @xxx
    var atMatches = text.match(/@[一-龥a-zA-Z0-9_\-]+/g);
    if (atMatches) tags = tags.concat(atMatches.map(function(t) { return t.substring(1).toLowerCase(); }));
    // 提取地名标记 #xxx
    var hashMatches = text.match(/#[一-龥a-zA-Z0-9_\-]+/g);
    if (hashMatches) tags = tags.concat(hashMatches.map(function(t) { return t.substring(1).toLowerCase(); }));
    // 去重
    return tags.filter(function(t, i) { return tags.indexOf(t) === i; });
  }

  function searchMemory(state, query, options) {
    options = options || {};
    var maxResults = options.maxResults || 10;
    var filterTags = options.filterTags || null;
    var memories = state.memories || [];
    
    // 1) 标签预过滤（如果指定了 filterTags）
    var candidateMemories = memories;
    if (filterTags && filterTags.length > 0) {
      var ftLower = filterTags.map(function(t) { return t.toLowerCase(); });
      candidateMemories = memories.filter(function(m) {
        if (!m.tags || !m.tags.length) return false;
        return ftLower.some(function(ft) { return m.tags.indexOf(ft) !== -1; });
      });
    }
    
    // 2) 如果没 query，直接返回候选集
    if (!query || !query.trim()) {
      return candidateMemories.slice(0, maxResults);
    }
    
    // 3) 模糊匹配
    var ql = query.toLowerCase();
    var scored = candidateMemories.map(function(m) {
      var score = 0;
      var memText = ((m.content || m.text || m.summary || '') + ' ' + (m.title || '')).toLowerCase();
      if (memText.indexOf(ql) !== -1) score += 10;
      else {
        var words = ql.split(/\s+/);
        for (var wi = 0; wi < words.length; wi++) {
          if (words[wi] && memText.indexOf(words[wi]) !== -1) score += 3;
        }
      }
      // 情感记忆加权
      if (m.emotional) score += 2;
      return { mem: m, score: score };
    });
    
    scored.sort(function(a, b) { return b.score - a.score; });
    return scored.filter(function(s) { return s.score > 0; }).slice(0, maxResults).map(function(s) { return s.mem; });
  }

  // ★ v3.0.0: 冷热分离判断
  function isHotMemory(memory, currentRound, config) {
    config = config || {};
    var rounds = config.hotMemoryRounds || 50;
    var importanceMin = config.hotMemoryImportance || 2;
    if (memory.lastAccessRound && (currentRound - memory.lastAccessRound) <= rounds) return true;
    if (memory.importance >= importanceMin) return true;
    return false;
  }

  function addMemory(state, memory) {
    if (!state) return;
    // ★ v3.0.0: 自动提取标签
    if (!memory.tags) {
      var memText = (memory.content || memory.text || '') + ' ' + (memory.title || '');
      memory.tags = extractTags(memText);
    }
    // ★ v3.0.0: 冷热分离 — 记录访问轮次
    if (memory.lastAccessRound === undefined) memory.lastAccessRound = state.round || 0;
    if (memory.type === 'round') {
      state.memories.unshift(memory);
      if (state.memories.length > 500) state.memories.pop();
    } else if (memory.type === 'chapter_summary') {
      state.chapterSummaries.unshift(memory);
      if (state.chapterSummaries.length > 50) state.chapterSummaries.pop();
    } else if (memory.type === 'volume_summary') {
      state.volumeSummaries.unshift(memory);
      if (state.volumeSummaries.length > 20) state.volumeSummaries.pop();
    }
    saveState(state);
  }

  function updateEmotion(state, npc, attitude, level, reason) {
    if (!npc) return;
    if (!state.emotionMap) state.emotionMap = {};
    if (!state.emotionMap[npc]) {
      state.emotionMap[npc] = { attitude: '中立', level: '陌生人', reasons: [] };
    }
    if (attitude) {
      state.emotionMap[npc].attitude = attitude;
    } else if (attitude !== null) {
      // 空保护：如果传了 null/undefined 但不更新
    }
    if (level) {
      state.emotionMap[npc].level = level;
    }
    if (reason && !state.emotionMap[npc].reasons.includes(reason)) {
      state.emotionMap[npc].reasons.push(reason);
      if (state.emotionMap[npc].reasons.length > 10) {
        state.emotionMap[npc].reasons = state.emotionMap[npc].reasons.slice(-10);
      }
    }
    saveState(state);
  }

  // 快速汇总情感状态（用于注入）
  function getEmotionSummary(state, limit = 5) {
    if (!state.emotionMap) return '无';
    const entries = Object.entries(state.emotionMap);
    // 按态度强度排序：敌意 > 信任 > 警惕 > 中立
    const attitudeOrder = { '敌意': 0, '不共戴天': 0, '警惕': 1, '信任': 2, '友善': 3, '友好': 3, '中立': 4 };
    entries.sort((a, b) => {
      const ao = attitudeOrder[a[1].attitude] ?? 5;
      const bo = attitudeOrder[b[1].attitude] ?? 5;
      return ao - bo;
    });
    return entries.slice(0, limit).map(([n, e]) => `${n}:${e.attitude}(${e.level})`).join(', ') || '无';
  }

  // 定期清理：移除低重要度的过期记忆碎片
  function cleanupState(state) {
    if (!state.memories) return;
    const now = state.round;
    const before = state.memories.length;
    state.memories = state.memories.filter(m => {
      if (m.importance >= 3) return true;
      if (m.importance === 2) return now - m.round < 50;
      if (m.importance === 1) return now - m.round < 30;
      return true;
    });
    const after = state.memories.length;
    if (before !== after) {
      saveState(state);
      console.log(`[World Engine Core] 清理记忆：${before} → ${after} (移除了 ${before - after} 条)`);
    }
  }

