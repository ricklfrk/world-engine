function autoBackup(state) {
  if (!state || !state.round) return;
  if (state.round % 50 !== 0) return; // 每 50 轮自动备份
  const backupKey = 'world_engine_auto_backups';
  let backups = [];
  try { backups = JSON.parse(window.WORLD_ENGINE_STORAGE.getItem(backupKey) || '[]'); } catch(e) {}
  const entry = { round: state.round, timestamp: Date.now(), state: JSON.parse(JSON.stringify(state)) };
  backups.push(entry);
  // 最多保留 3 份，循环覆盖
  if (backups.length > 3) backups = backups.slice(-3);
  window.WORLD_ENGINE_STORAGE.setItem(backupKey, JSON.stringify(backups));
}

// ★ v3.0.0: 个人传奇
function generateLegend(worldState, characterName) {
  if (!worldState || !characterName) return '';
  if (!worldState.legends) worldState.legends = {};
  if (worldState.legends[characterName]) return worldState.legends[characterName];

  var achievements = worldState.achievements || [];
  var unlocked = [];
  for (var ai = 0; ai < achievements.length; ai++) {
    if (achievements[ai].unlocked) unlocked.push(achievements[ai].name || achievements[ai].id);
  }
  var round = worldState.round || 0;
  var combat = worldState.combat || {};

  var lines = [];
  lines.push(characterName + '的传奇');
  lines.push('历经' + round + '轮风云变幻');
  if (unlocked.length > 0) lines.push('达成成就: ' + unlocked.slice(0, 5).join('、'));
  if ((combat.totalBattles || 0) > 0) lines.push('经历战斗: ' + combat.totalBattles + ' 场');
  if ((combat.wins || 0) > 0) lines.push('胜利次数: ' + combat.wins);

  var legend = lines.join(' · ');
  worldState.legends[characterName] = legend;
  return legend;
}

